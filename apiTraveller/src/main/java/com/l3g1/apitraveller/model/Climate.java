package com.l3g1.apitraveller.model;

// This enum represents different climate types that destinations may have.
public enum Climate {
    TROPICAL, // Characterized by warm temperatures and high humidity, typically found near the equator.
    DESERT, // Characterized by low precipitation and high temperatures, often with large temperature fluctuations between day and night.
    POLAR, // Characterized by extrem cold temperatures, typically found near the Earth's poles.
    MEDITERRANEAN, // Characterized by warm, dry summers and mild, wet winters, typical of regions bordering the Mediterranean Sea.
    TEMPERATE, // Characterized by moderate temperatures and distinct seasons, often found in middle latitudes.
    ALL, // Special value indicating all types of climates.
    OTHER; // Other miscellaneous climate types not covered by the above categories.
}

