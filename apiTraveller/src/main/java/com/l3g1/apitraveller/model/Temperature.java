package com.l3g1.apitraveller.model;

// This enum represents different temperature ranges.
public enum Temperature {
	HOT, // Extremely high temperatures.
	WARM, // Moderately high temperatures.
	MILD, // Pleasant and moderate temperatures.
	TEMPERATE, // Neither too hot nor too cold, moderate temperatures.
	COOL, // Moderately low temperatures.
	COLD, // Extremely low temperatures.
	ALL; // Special value indicating all temperature ranges.
}
